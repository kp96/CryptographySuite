public class Pair {
    private int first;
    private boolean second;
    public Pair(int first, boolean second) {
    	this.first = first;
    	this.second = second;
    }
    public int getFirst() {
    	return this.first;
    }
    public boolean getSecond() {
    	return this.second;
    }
}